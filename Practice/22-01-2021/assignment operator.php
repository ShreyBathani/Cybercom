<?php

//Assignment operator
echo "<h1>Assignment operator</h1><br>";
$x = 20;
echo "$x<br>"; // Outputs: 10
 
$x += 30;
echo "$x<br>"; // Outputs: 50
 
$x -= 20;
echo "$x<br>"; // Outputs: 30
 
$x *= 5;
echo "$x<br>"; // Outputs: 150
 
$x /= 10;
echo "$x<br>"; // Outputs: 15
 
$x = 100;
$x %= 15;
echo "$x<br>"; // Outputs: 10
